// Reads the repository's root CHANGELOG.md and writes it as a docs page,
// prepending Docusaurus frontmatter so it appears in the Reference sidebar.
//
// Run automatically before `npm start` and `npm run build` so the docs site
// always reflects the canonical changelog without manual duplication.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..', '..');
const sourcePath = join(repoRoot, 'CHANGELOG.md');
const targetPath = join(here, '..', 'content', 'reference', 'changelog.md');

const frontmatter = `---
title: Changelog
sidebar_position: 5
description: Release history of RDFArchitect, mirrored from the repository root CHANGELOG.md.
---

{/* This file is auto-generated by docs/scripts/sync-changelog.mjs from the repository root CHANGELOG.md. Do not edit by hand. */}

`;

const source = readFileSync(sourcePath, 'utf8');

mkdirSync(dirname(targetPath), { recursive: true });
writeFileSync(targetPath, frontmatter + source.trimStart(), 'utf8');

console.log(`[sync-changelog] wrote ${targetPath}`);
